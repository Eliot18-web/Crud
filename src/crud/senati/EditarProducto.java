    private void btnModicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModicarActionPerformed
        // TODO add your handling code here:
        Conexion cn = new Conexion();
        String query = "INSERT INTO productos(id, name, , category, brand) VALUES(?, ?, ?, ?)";

        int id = Integer.parseInt(txtID.getText());
        String nombre = txtName.getText();
        String categoria = txtCategory.getText();
        String marca = txtBrand.getText();
        try {
            PreparedStatement ps = cn.conectar().prepareStatement(query);

            ps.setInt(1, id);
            ps.setString(2, nombre);
            ps.setString(3, categoria);
            ps.setString(4, marca);

            if (ps.execute()){
                System.out.println("Registro insertado correctamente");
            }

            dispose();
        }  catch (SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnModicarActionPerformed

